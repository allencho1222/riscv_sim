#define LBU       0b00000000000000000100000000000011
#define LHU       0b00000000000000000101000000000011

// STORE instructions
#define SB        0b00000000000000000000000000100011
#define SH        0b00000000000000000001000000100011
#define SW        0b00000000000000000010000000100011

// SHIFT instructions
#define SLL       0b00000000000000000001000000110011
#define SLLI      0b00000000000000000001000000010011
#define SRL       0b00000000000000000101000000110011
#define SRLI      0b00000000000000000101000000010011
#define SRA       0b01000000000000000101000000110011
#define SRAI      0b01000000000000000101000000010011

// ARITHMETIC instructions
#define ADD       0b00000000000000000000000000110011
#define ADDI      0b00000000000000000000000000010011
#define SUB       0b01000000000000000000000000110011
#define LUI       0b00000000000000000000000000110111
#define AUIPC     0b01000000000000000101000000010111

// LOGICAL instructions
#define XOR       0b00000000000000000100000000110011
#define XORI      0b00000000000000000100000000010011
#define OR        0b00000000000000000110000000110011
#define ORI       0b00000000000000000110000000010011
#define AND       0b00000000000000000111000000110011
#define ANDI      0b00000000000000000111000000010011

// COMPARE instructions
#define SLT       0b00000000000000000010000000110011
#define SLTI      0b00000000000000000010000000010011
#define SLTU      0b00000000000000000011000000110011
#define SLTIU     0b00000000000000000011000000010011

// BRNACH instructions
#define BEQ       0b00000000000000000000000001100011
#define BNE       0b00000000000000000001000001100011
#define BLT       0b00000000000000000100000001100011
#define BGE       0b00000000000000000101000001100011
#define BLTU      0b00000000000000000110000001100011
#define BGEU      0b00000000000000000111000001100011

// JUMP AND LINK instructions
#define JAL       0b00000000000000000000000001101111
#define JALR      0b00000000000000000000000001100111

