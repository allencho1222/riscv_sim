// LOAD instructions
#define LB        0b0000 0000 0000 00000 000 00000 0000011
#define LH        0b0000 0000 0000 00000 001 00000 0000011
#define LW        0b0000 0000 0000 00000 010 00000 0000011
#define LBU       0b0000 0000 0000 00000 100 00000 0000011
#define LHU       0b0000 0000 0000 00000 101 00000 0000011

// STORE instructions
#define SB        0b0000 0000 0000 00000 000 00000 0100011
#define SH        0b0000 0000 0000 00000 001 00000 0100011
#define SW        0b0000 0000 0000 00000 010 00000 0100011

// SHIFT instructions
#define SLL       0b0000000 00000 00000 001 00000 0110011
#define SLLI      0b0000000 00000 00000 001 00000 0010011
#define SRL       0b0000000 00000 00000 101 00000 0110011
#define SRLI      0b0000000 00000 00000 101 00000 0010011
#define SRA       0b0100000 00000 00000 101 00000 0110011
#define SRAI      0b0100000 00000 00000 101 00000 0010011

// ARITHMETIC instructions
#define ADD       0b0000000 00000 00000 000 00000 0110011
#define ADDI      0b0000000 00000 00000 000 00000 0010011
#define SUB       0b0100000 00000 00000 000 00000 0110011
#define LUI       0b0000000 00000 00000 000 00000 0110111
#define AUIPC     0b0100000 00000 00000 101 00000 0010111

// LOGICAL instructions
#define XOR       0b0000000 00000 00000 100 00000 0110011
#define XORI      0b0000000 00000 00000 100 00000 0010011
#define OR        0b0000000 00000 00000 110 00000 0110011
#define ORI       0b0000000 00000 00000 110 00000 0010011
#define AND       0b0000000 00000 00000 111 00000 0110011
#define ANDI      0b0000000 00000 00000 111 00000 0010011

// COMPARE instructions
#define SLT       0b0000000 00000 00000 010 00000 0110011
#define SLTI      0b0000000 00000 00000 010 00000 0010011
#define SLTU      0b0000000 00000 00000 011 00000 0110011
#define SLTIU     0b0000000 00000 00000 011 00000 0010011

// BRNACH instructions
#define BEQ       0b0000000 00000 00000 000 00000 1100011
#define BNE       0b0000000 00000 00000 001 00000 1100011
#define BLT       0b0000000 00000 00000 100 00000 1100011
#define BGE       0b0000000 00000 00000 101 00000 1100011
#define BLTU      0b0000000 00000 00000 110 00000 1100011
#define BGEU      0b0000000 00000 00000 111 00000 1100011

// JUMP AND LINK instructions
#define JAL       0b0000000 00000 00000 000 00000 1101111
#define JALR      0b0000000 00000 00000 000 00000 1100111

#define OP_MASK	  0b0000000 00000 00000 000 00000 1111111
#define FN7_MASK  0b1111111 00000 00000 000 00000 0000000
#define FN3_MASK  0b0000000 00000 00000 111 00000 0000000
